package task_250918;

public class Login {

	private String email;
	private String pw;
	public Login(String email, String pw) {
		super();
		this.email = email;
		this.pw = pw;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	
	

}
